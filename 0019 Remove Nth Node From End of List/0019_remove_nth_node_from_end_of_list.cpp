/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    ListNode* removeNthFromEnd(ListNode* head, int n) {
        int sz = 0;
        ListNode* temp = head;
        while(temp) {
            temp=temp->next;
            sz++;
        }
        if(sz==1) return nullptr;
        temp = head;
        if(sz == n) {
            head = head->next;
            delete temp;
            return head;
        }
        sz -= (n+1);
        while(sz>0) {
            temp = temp->next;
            sz--;
        }
        ListNode* dlt = temp->next;
        temp->next = temp->next->next;
        delete dlt;
        return head;
    }
};

