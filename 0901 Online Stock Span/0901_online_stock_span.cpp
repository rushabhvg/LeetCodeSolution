class StockSpanner {
    stack<pair<int, int>> st;
public:
    StockSpanner() {
        // DNC;
    }
    
    int next(int price) {
        int res = 1;
        while(st.size() && st.top().first <= price) {
            res += st.top().second;
            st.pop();
        }
        st.push({price, res});
        return res;
    }
};

