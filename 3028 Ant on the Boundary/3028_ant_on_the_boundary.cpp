class Solution {
public:
    int returnToBoundaryCount(vector<int>& nums) {
        int cnt = 0;
        for(int i=1; i<nums.size(); i++) {
            nums[i] += nums[i-1];
            cnt += nums[i]==0 ? 1 : 0;
        }
        return cnt;
    }
};

