/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    ListNode *getIntersectionNode(ListNode *headA, ListNode *headB) {
        ListNode *arrA = headA;
        ListNode *arrB = headB;
        if(arrA==NULL || arrB==NULL) return NULL;
        while(arrA!=NULL && arrB!=NULL && arrA!=arrB) {
            arrA = arrA->next;
            arrB = arrB->next;
            if(arrA == arrB) return arrA;
            if(arrA==NULL) arrA = headB;
            if(arrB==NULL) arrB = headA;
        }
        return arrA;
    }
};

