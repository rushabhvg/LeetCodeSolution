/**
 * @param {number[]} nums
 * @param {Function} fn
 * @param {number} init
 * @return {number}
 */
var reduce = function(nums, fn, init) {
    let cnt = init;
    for(let i = 0; i<nums.length; i++) {
        cnt = fn(cnt, nums[i]);
    }
    return cnt;
};

