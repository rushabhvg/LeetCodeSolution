class MyStack {
    queue<int> q;
public:
    MyStack() {
        // NOTHING
    }
    
    void push(int x) {
        q.push(x);
        int n = q.size()-1;
        for(int i=0; i<n; i++) {
            q.push(q.front());
            q.pop();
        }
    }
    
    int pop() {
        int res = top();
        q.pop();
        return res;
    }
    
    int top() {
        return q.front();
    }
    
    bool empty() {
        return q.empty();
    }
};

