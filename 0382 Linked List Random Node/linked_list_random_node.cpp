/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    ListNode *temp;
    ListNode *curr;
    Solution(ListNode* head) {
        temp = head;
        curr = head;
    }
    int getRandom() {
        int cnt = 1, chosenValue = temp->val;
        curr = temp;
        while(curr != nullptr) {
            if(rand()%cnt==cnt-1) {
                chosenValue = curr->val;
            }
            cnt+=1;
            curr = curr->next;
        }
        return chosenValue;
    }
};

