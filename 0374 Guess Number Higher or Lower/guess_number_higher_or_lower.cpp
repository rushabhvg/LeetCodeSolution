/** 
 * Forward declaration of guess API.
 * @param  num   your guess
 * @return 	     -1 if num is higher than the picked number
 *			      1 if num is lower than the picked number
 *               otherwise return 0
 * int guess(int num);
 */

class Solution {
public:
    int guessNumber(int n) {
        int start = 0;
        int last = n;
        int mid;
        while(start<=last) {
            mid = start + (last-start)/2;
            int ans = guess(mid);
            if(ans==-1) {
                last = mid-1;
            } else if (ans==1) {
                start = mid+1;
            } else {
                return mid;
            }
        }
        return last+1;
    }
};

