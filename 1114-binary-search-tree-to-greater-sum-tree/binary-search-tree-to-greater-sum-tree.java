/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    int tot = 0;
    public void trav(TreeNode root) {
        if(root==null) return ;
        trav(root.right);
        tot += root.val;
        root.val = tot;
        trav(root.left);
        return ;
    }
    public TreeNode bstToGst(TreeNode root) {
        trav(root);
        return root;
    }
}

