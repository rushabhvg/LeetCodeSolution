class Solution {
public:
    int firstUniqChar(string s) {
        map<char, int> ans;
        for(auto& c: s) {
            ans[c]++;
        }
        for(int i=0; i<s.size(); i++) {
            if(ans[s[i]]==1) return i;
        }
        return -1;
    }
};

